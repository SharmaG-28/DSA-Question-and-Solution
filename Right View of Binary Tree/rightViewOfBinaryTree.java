class Solution{
    //Function to return list containing elements of right view of binary tree.
    ArrayList<Integer> rightView(Node node) {
        //add code here.
        ArrayList<Integer> result = new ArrayList<>();
        if (node == null)
            return result;

        // Using a queue to perform level order traversal
        Queue<Node> queue = new LinkedList<>();
        queue.offer(node);

        while (!queue.isEmpty()) {
            int size = queue.size();

            // Traverse all nodes at current level
            for (int i = 0; i < size; i++) {
                Node current = queue.poll();

                // If it's the last node at this level, add its value to the result
                if (i == size - 1)
                    result.add(current.data);

                // Add left and right children of current node to the queue
                if (current.left != null)
                    queue.offer(current.left);
                if (current.right != null)
                    queue.offer(current.right);
            }
        }
        return result;
    }
}

